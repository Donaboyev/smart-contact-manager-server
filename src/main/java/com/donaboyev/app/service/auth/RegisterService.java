    private final ConfirmationTokenService confirmationTokenService;
    private final UserService userService;
    private final PasswordEncoder passwordEncoder;
    private final EmailSender emailSender;

    public MessageResponse register(RegisterRequest request) {
        User user = new User(request.getFirstName(),request.getLastName(),request.getEmail(),request.getPassword());
        boolean userExists = userService.findByEmail(user.getEmail()).isPresent();
        if (userExists) {
            User existedUser = userService.findByEmail(user.getEmail()).get();
            if (existedUser.getEnabled()) {
                return new MessageResponse("Email is already in use!");
            }
            ConfirmationToken token = confirmationTokenService.getTokenByUser(existedUser)
                    .orElseThrow(() -> new IllegalStateException("Confirmation token not sent to this email"));
            boolean tokenExpired = token.getExpiresAt().isBefore(LocalDateTime.now());
            if (tokenExpired) {
                confirmationTokenService.deleteToken(token);
                userService.deleteUser(existedUser.getId());
                return registerUser(user);
            }
            return new MessageResponse("Email is in use! But not verified yet. Try again later");
        }
        return registerUser(user);
    }

    @Transactional
    public String confirmToken(String token) {
        ConfirmationToken confirmationToken = confirmationTokenService.getToken(token)
                .orElseThrow(() -> new IllegalStateException("Token not found"));
        if (confirmationToken.getConfirmedAt() != null) {
            throw new IllegalStateException("Email already confirmed");
        }
        LocalDateTime expiredAt = confirmationToken.getExpiresAt();
        if (expiredAt.isBefore(LocalDateTime.now())) {
            throw new IllegalStateException("Token expired");
        }
        confirmationTokenService.setConfirmedAt(token);
        userService.enableUser(confirmationToken.getUser().getEmail());
        return "Confirmed";
    }

    private MessageResponse registerUser(User user) {
        String encodedPassword = passwordEncoder.encode(user.getPassword());
        user.setPassword(encodedPassword);
        String token = UUID.randomUUID().toString();
        ConfirmationToken confirmationToken = new ConfirmationToken(
                token,
                LocalDateTime.now(),
                LocalDateTime.now().plusMinutes(15),
                user
        );
        String link = "http://localhost:8080/api/auth/confirm?token=" + token;
        if (emailSender.send(user.getEmail(), buildEmail(user.getFirstName(), link))) {
            user.setUserRole(UserRole.ROLE_USER);
            userService.saveUser(user);
            confirmationTokenService.saveConfirmationToken(confirmationToken);
        }
        return new MessageResponse(token);
    }

